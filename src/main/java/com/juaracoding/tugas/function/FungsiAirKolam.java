package com.juaracoding.tugas.function;

public class FungsiAirKolam {
	
	public double hitungKeliling(double panjang, double lebar, double tinggi) {
		return 4 * (panjang + lebar + tinggi);
	}
	
	public double hitungVolume(double panjang, double lebar, double tinggi) {
		return panjang * lebar * tinggi;
	}
}
